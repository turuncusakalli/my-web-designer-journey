// Ders 3: Değişken Tipleri

// Primitive Types

// String
let firstName = 'Çınar';
console.log(typeof firstName) // değişken tipini sorguluyoruz. string

// Number
let age = 20; 
console.log(typeof age)  // number

let money = 100.5;
console.log(typeof money)  // number

// Boolean
let isActive = false;
console.log(isActive) // boolean

// null
let job = null
console.log(job) // object

// undefined
let car;
console.log(typeof car) // undefined



// Reference Types: Objects

// Array - Dizi

let names = ['Ali','Ahmet','Can']
console.log(names) // object

// Object Literals - Sözlükler gibi

let address= {
   city: 'Kocaeli',
   country: 'Türkiye'
}

console.log(address) // object

// Functions

var calculateAge = function(){
   return 0;
}

console.log(typeof calculateAge) // function



